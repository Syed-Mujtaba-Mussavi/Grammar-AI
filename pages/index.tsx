import Head from "next/head";
import { useState } from "react";
import { Inter } from "next/font/google";
import confetti from "canvas-confetti";
import toast from "react-hot-toast";

const inter = Inter({
  weight: ["400", "900", "600", "500"],
  display: "swap",
  subsets: ["latin"],
});

import { Hero, Navbar, Reasons, Generated } from "@/components";
import Footer from "@/components/Footer";
import { AnimatePresence } from "framer-motion";

export default function Home() {
  const [promptValue, setPromptValue] = useState("");
  const [generated, setGenerated] = useState("");
  const [loader, setLoader] = useState("Generate");

  const prompt = `I will give you a sentence, Please correct it or rewrite it to a professional manner, the following sentence is: "${promptValue}"`;

  const generating = async () => {
    if (promptValue.length == 0 || loader == "Generating...") return;

    setLoader("Generating...");
    const res = await fetch("/api/generate", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ prompt }),
    });

    const data = await res.json();

    setGenerated(data.choices[0].text);
    confetti();
    setLoader("Generate Again");
  };

  const copingText = (value: string) => {
    navigator.clipboard.writeText(value);
    toast("âœ… Copied");
  };

  return (
    <>
      <Head>
        <title>Grammar AI</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.svg" />
      </Head>
      <main className={`${inter.className}`}>
        <Navbar />

        <Hero
          promptValue={promptValue}
          setPromptValue={setPromptValue}
          generating={generating}
          loader={loader}
        />

        <AnimatePresence mode="wait" initial={false}>
          {generated.length !== 0 && (
            <Generated
              promptValue={promptValue}
              generated={generated}
              setGenerated={setGenerated}
              copingText={copingText}
            />
          )}
        </AnimatePresence>

        <Reasons />
        <Footer />
      </main>
    </>
  );
}
